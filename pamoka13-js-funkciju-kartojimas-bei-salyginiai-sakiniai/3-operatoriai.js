// Pagrindinė funkcija
window.onload = function() {
    // Sumos operatorius +
    console.log(5 + 8);
    console.log("5" + "8");

    // Atimties operatorius -
    console.log(5 - 8);

    // Daugybos operatorius *
    console.log(5 * 8);

    // Dalybos operatorius /
    console.log(5 / 8);

    // Teksto konvertavimas į skaičių operatorius +
    console.log(+"8");
    console.log(+"tekstas");
    console.log(+"5" + +"8");

    // Skaičių konvertavimas į tekstą (čia ne operatorius, tiesiog papildomai)
    console.log("" + 8 + 5);

    // Kėlimo laipsniu operatorius
    console.log(2**4);       // 2^4
    console.log(5**3);       // 5^3
    console.log(10**5);     // 10^5   
    console.log(7**2);      // 7^2

    // Liekanos operatorius
    console.log(17 % 5);            // liekana dalinant 17 iš 5
    console.log(20 % 10);           
    console.log(51 % 2);


}

